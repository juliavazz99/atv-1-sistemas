#include <stdio.h>
// A diretiva #include <stdio.h> serve para incluir a biblioteca padrão
// de entrada e saída do C. Ela permite usar funções como printf e scanf.

int main(void)
// O programa começa a ser executado na função main.
// É o ponto de entrada obrigatório de um programa em C.
{
    printf("Hello, World!\n");
    // A função printf imprime o texto na tela.
    // O \n é um caractere especial que faz a quebra de linha.

    return 0;
    // O return 0 indica que o programa terminou corretamente.
    // Ele devolve o valor 0 para o sistema operacional.
}
